def plus(main, plusNumber):
    print(main+plusNumber)

def negative(main, negativeNumber):
    print(main - negativeNumber)

def multiply(main, multiplier):
    print(main * multiplier)

def divide(main, divider):
    print(main / divider)

print("+ - * / with 20 and 5")
plus(20,5)
negative(20,5)
multiply(20,5)
divide(20,5)

